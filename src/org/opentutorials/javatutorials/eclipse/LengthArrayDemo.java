package org.opentutorials.javatutorials.eclipse;

public class LengthArrayDemo {

	public static void main(String[] args) {
		String[] classGroup = new String[4];
			classGroup[0] = "이동준";
			System.out.println(classGroup.length);
			classGroup[1] = "정영운";
			System.out.println(classGroup.length);
			classGroup[2] = "동운이";
			System.out.println(classGroup.length);

	}

}
